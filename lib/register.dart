  void signUp(String email, String password, String rool) async {
  if (_formkey.currentState!.validate()) {
    try {
      await _auth.createUserWithEmailAndPassword(
        email: email,
        password: password,
      );
      await postDetailsToFirestore(email, rool);
    } catch (e) {
      setState(() {
        showProgress = false;
      });
      print('Error signing up: $e');
      // Show error message to the user
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Failed to register. Please try again.')),
      );
    }
  }
}

  postDetailsToFirestore(String email, String rool) async {
  FirebaseFirestore firebaseFirestore = FirebaseFirestore.instance;
  var user = _auth.currentUser;
  if (user == null) {
    print('Error: User is not logged in!');
    return;
  }

  CollectionReference ref = FirebaseFirestore.instance.collection('users');

  try {
    await ref.doc(user.uid).set({
      'name' : name.text,
      'email': emailController.text,
      'rool': rool,
      'phone': mobileController.text,
      'pgName': pgNameController.text,
      'address': addressController.text,
      'guardianPhone': guardianPhoneController.text,
    });
    // Navigate to login page after successful registration
    Navigator.pushReplacement(
      context,
      MaterialPageRoute(builder: (context) => LoginSignupPage()),
    );
  } catch (e) {
    print('Error posting details to Firestore: $e');
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(content: Text('Failed to register. Please try again.')),
    );
  } finally {
    setState(() {
      showProgress = false;
    });
  }
}
